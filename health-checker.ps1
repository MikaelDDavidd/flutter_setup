function Repair-Environment {
    param([array]$Issues)
    
    if (-not $Fix) {
        return
    }
    
    Write-Header "TENTANDO CORRIGIR PROBLEMAS"
    
    foreach ($issue in $Issues) {
        Write-ColorMessage "üîß Tentando corrigir: $issue" "Info"
        
        switch -Wildcard ($issue) {
            "*JAVA_HOME*" {
                # Tentar encontrar e configurar JAVA_HOME
                $javaPath = "C:\DevPrograms\java\current"
                if (Test-Path $javaPath) {
                    [Environment]::SetEnvironmentVariable("JAVA_HOME", $javaPath, "User")
                    Write-ColorMessage "‚úÖ JAVA_HOME configurado para: $javaPath" "Success"
                }
            }
            
            "*ANDROID_HOME*" {
                # Tentar encontrar e configurar ANDROID_HOME
                $possiblePaths = @(
                    "${env:LOCALAPPDATA}\Android\Sdk",
                    "${env:ProgramFiles}\Android\sdk",
                    "C:\Android\sdk"
                )
                
                foreach ($path in $possiblePaths) {
                    if (Test-Path $path) {
                        [Environment]::SetEnvironmentVariable("ANDROID_HOME", $path, "User")
                        [Environment]::SetEnvironmentVariable("ANDROID_SDK_ROOT", $path, "User")
                        Write-ColorMessage "‚úÖ Android SDK configurado para: $path" "Success"
                        break
                    }
                }
            }
            
            "*Flutter n√£o encontrado*" {
                # Verificar se Flutter existe mas n√£o est√° no PATH
                $flutterPath = "C:\DevPrograms\flutter"
                if (Test-Path $flutterPath) {
                    [Environment]::SetEnvironmentVariable("FLUTTER_HOME", $flutterPath, "User")
                    
                    $currentPath = [Environment]::GetEnvironmentVariable("PATH", "User")
                    $flutterBin = Join-Path $flutterPath "bin"
                    if ($currentPath -notlike "*$flutterBin*") {
                        $newPath = "$currentPath;$flutterBin"
                        [Environment]::SetEnvironmentVariable("PATH", $newPath, "User")
                        Write-ColorMessage "‚úÖ Flutter adicionado ao PATH" "Success"
                    }
                }
            }
            
            default {
                Write-ColorMessage "‚ö†Ô∏è Corre√ß√£o n√£o implementada para: $issue" "Warning"
            }
        }
    }
    
    Write-ColorMessage "üîÑ Reinicie o PowerShell para aplicar as corre√ß√µes" "Warning"
}

